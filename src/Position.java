
public class Position {

	// ================================================================================================
	// ATTRIBUTES
	// ================================================================================================
	private int line;
	private int row;
	
	// ================================================================================================
	// CONSTRUCTOR
	// ================================================================================================
	public Position(int x, int y) {
		this.line = x;
		this.row = y;
	}

	// ================================================================================================
	// PUBLIC FUNCTIONS
	// ================================================================================================
	public int getLine() {
		return this.line;
	}
	
	public int getRow() {
		return this.row;
	}

	public String toString() {
		return "("+this.line+", "+this.row+")";
	}

}
